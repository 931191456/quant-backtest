def _fetch_with_retry(func, *args, max_retries=1, **kwargs):
    """带重试的数据获取"""
    for attempt in range(max_retries + 1):
        try:
            return func(*args, **kwargs)
        except Exception as e:
            if attempt < max_retries:
                time.sleep(0.5)
                continue
            raise e


